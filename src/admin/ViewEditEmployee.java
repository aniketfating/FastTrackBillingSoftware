    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String name1=jTextField1.getText();
      String email1=jTextField2.getText();
      String pass1=jTextField3.getText();
      String phoneNo=jTextField4.getText();
      
      String gender="";
      if(jRadioButton1.isSelected())
      {
          gender="M";
      }
      if(jRadioButton2.isSelected())
      {
          gender="F";
      }
      
      Register reg=new Register();
      
      reg.setName(name1);
      reg.setEmail(email1);
      reg.setPassword(pass1);
      reg.setPhoneno(phoneNo);
      reg.setGender(gender);
      
      int i=DB_Operation.updateEmployee(reg);
      if(i>0)
      {
          JOptionPane.showMessageDialog(rootPane, "Values Updated Succesfully ");
          new ViewEditEmployee().setVisible(true);
          setVisible(false);
      }
      else
      {
          JOptionPane.showMessageDialog(rootPane, "Employee not registered due to some error","Update Error",JOptionPane.ERROR_MESSAGE);
      }
      
    }//GEN-LAST:event_jButton1ActionPerformed

