    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
    
        String name1,email1;
        int srno=0;
        
      try
      {
           ResultSet rs= DB_Operation.showAllEmpData();
        if(rs!=null)
        {
            while(rs.next())
            {  
                srno=srno+1;
                name1=rs.getString("name");
                email1=rs.getString("email");
                
                row=new Object[]{srno,name1,email1};
                dtm.addRow(row);
            }
        }
        
      }
      catch(Exception e)
      {
          System.out.println(e);
      }
    }//GEN-LAST:event_formWindowOpened

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
      int row= jTable1.getSelectedRow();
      String selectedEmail=(String) jTable1.getValueAt(row, 2);
      
      ResultSet rs=DB_Operation.getSelectedEmployee(selectedEmail);
      
     try
     {
       if(rs!=null)
        {
          if(rs.next())
          {
              jTextField1.setText(rs.getString("name"));
              jTextField2.setText(rs.getString("email"));
              jTextField3.setText(rs.getString("password"));
              jTextField4.setText(rs.getString("phone_no"));
              String gender=rs.getString("gender");
              
           if(gender.equals("m"))
          {
              jRadioButton1.setSelected(true);
          }
          else
          {
              jRadioButton2.setSelected(true);
          }
          }
              
       }
     }
     catch(Exception e)
     {
         System.out.println(e);
     }
      
    }//GEN-LAST:event_jTable1MouseClicked

